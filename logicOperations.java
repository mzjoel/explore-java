public class logicOperations {
    public static void main(String[] args){
        boolean a = true;
        boolean b = false;
        boolean c;

        //AND (&&)
        c = a && b;
        System.out.println("a && b = "+c);
        //OR 
        c = a || b;
        System.out.println("a || b= "+ c);
        //Negasi 
        System.out.println("!a = "+!a);
    }
}
